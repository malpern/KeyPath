prepare_custom_rule_fixtures() {
  mkdir -p "$CONFIG_DIR"
  printf '[]\n' >"$CONFIG_DIR/CustomRules.json"

  "$CLI" rule add z x --on-conflict replace --json --quiet >/dev/null
  "$CLI" rule add q --tap q --hold lctl --tap-timeout 180 --on-conflict replace --json --quiet >/dev/null
  "$CLI" rule add grave --action '{"hyper":{}}' --on-conflict replace --json --quiet >/dev/null
  "$CLI" rule add f13 f14 --on-conflict replace --json --quiet >/dev/null
  "$CLI" rule add v --action '{"launchApp":{"name":"TextEdit","bundleId":"com.apple.TextEdit"}}' --on-conflict replace --json --quiet >/dev/null
  "$CLI" rule add b --action '{"openURL":{"_0":"https://example.com"}}' --on-conflict replace --json --quiet >/dev/null
  "$CLI" rule add n --action '{"systemAction":{"id":"mission-control"}}' --on-conflict replace --json --quiet >/dev/null
  "$CLI" rule add m --action '{"windowAction":{"position":"left"}}' --on-conflict replace --json --quiet >/dev/null
  "$CLI" rule add slash --action '{"activateLayer":{"name":"nav"}}' --on-conflict replace --json --quiet >/dev/null
  "$CLI" rule add comma --action '{"keystroke":{"key":"comma"}}' --behavior '{"macro":{"outputs":[],"text":"qa","source":"text"}}' --on-conflict replace --json --quiet >/dev/null
}

