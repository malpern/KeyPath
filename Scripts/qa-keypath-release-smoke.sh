#!/usr/bin/env bash
set -euo pipefail

APP_PATH="${APP_PATH:-/Applications/KeyPath.app}"
CLI="${KEYPATH_CLI:-$APP_PATH/Contents/MacOS/keypath-cli}"
CONFIG_DIR="${KEYPATH_CONFIG_DIR:-$HOME/.config/keypath}"
CONFIG_PATH="$CONFIG_DIR/keypath.kbd"
CONFIG_REAL_DIR="$(python3 -c 'import os, sys; print(os.path.realpath(sys.argv[1]))' "$CONFIG_DIR")"
KANATA_BIN="${KEYPATH_KANATA_BIN:-$APP_PATH/Contents/Library/KeyPath/Kanata Engine.app/Contents/MacOS/kanata}"
TCP_HOST="${KEYPATH_TCP_HOST:-127.0.0.1}"
TCP_PORT="${KEYPATH_TCP_PORT:-37001}"
TCP_TIMEOUT_SECONDS="${KEYPATH_TCP_TIMEOUT_SECONDS:-20}"
RUN_LOG_GATE="${KEYPATH_QA_RUN_LOG_GATE:-1}"
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"

TMP_DIR="$(mktemp -d)"
BACKUP_DIR="$TMP_DIR/original-keypath-config"
CONFIG_EXISTED=0
START_EPOCH="$(date +%s)"

cleanup() {
  local status=$?
  set +e
  if [[ "$CONFIG_EXISTED" == "1" ]]; then
    mkdir -p "$CONFIG_REAL_DIR"
    find "$CONFIG_REAL_DIR" -mindepth 1 -maxdepth 1 -exec rm -rf {} +
    cp -a "$BACKUP_DIR"/. "$CONFIG_REAL_DIR"/
  else
    rm -rf "$CONFIG_REAL_DIR"
  fi
  if [[ -x "$CLI" && "$CONFIG_EXISTED" == "1" ]]; then
    "$CLI" config apply --json --quiet >/dev/null 2>&1 || "$CLI" service reload --json --quiet >/dev/null 2>&1 || true
  fi
  rm -rf "$TMP_DIR"
  exit "$status"
}
trap cleanup EXIT

require_executable() {
  if [[ ! -x "$1" ]]; then
    echo "error: required executable not found: $1" >&2
    exit 1
  fi
}

assert_contains() {
  local haystack="$1"
  local needle="$2"
  local label="$3"
  if [[ "$haystack" != *"$needle"* ]]; then
    echo "error: $label did not contain expected snippet:" >&2
    echo "  $needle" >&2
    exit 1
  fi
}

wait_for_tcp() {
  local probe="$TMP_DIR/tcp-probe.log"
  local deadline=$((SECONDS + TCP_TIMEOUT_SECONDS))
  while true; do
    if nc -vz -w 1 "$TCP_HOST" "$TCP_PORT" >"$probe" 2>&1; then
      return 0
    fi
    if (( SECONDS >= deadline )); then
      cat "$probe" >&2 || true
      echo "error: TCP did not become ready at ${TCP_HOST}:${TCP_PORT}" >&2
      exit 1
    fi
    sleep 1
  done
}

apply_and_verify() {
  local label="$1"
  echo "==> $label"
  "$CLI" config apply --json --quiet >/dev/null
  "$CLI" config check --json --quiet >/dev/null
  if [[ ! -f "$CONFIG_PATH" ]]; then
    echo "error: generated config missing: $CONFIG_PATH" >&2
    exit 1
  fi
  (cd "$CONFIG_DIR" && "$KANATA_BIN" --cfg "$(basename "$CONFIG_PATH")" --check >/dev/null)
  "$CLI" service reload --json --quiet >/dev/null
  wait_for_tcp
}

